import type { ReactNode } from 'react';

function H2({ children }: { children: ReactNode }) {
  return (
    <h2 className="text-xl sm:text-2xl font-bold text-slate-800 mt-12 mb-4 pb-3 border-b-2 border-blue-100">
      {children}
    </h2>
  );
}

function P({ children }: { children: ReactNode }) {
  return <p className="text-slate-700 leading-relaxed mb-4">{children}</p>;
}

function InfoBox({ children }: { children: ReactNode }) {
  return (
    <div className="bg-blue-50 border border-blue-200 rounded-xl p-5 my-6 text-sm text-blue-900 leading-relaxed">
      {children}
    </div>
  );
}

function FormulaBox({ formula, example }: { formula: string; example?: string }) {
  return (
    <div className="bg-slate-800 text-white rounded-xl p-5 my-6 font-mono text-sm">
      <p className="mb-1 text-slate-400 text-xs font-sans">計算式</p>
      <p className="text-lg font-bold mb-2">{formula}</p>
      {example && (
        <>
          <p className="text-slate-400 text-xs font-sans mt-3 mb-1">計算例</p>
          <p className="text-green-300">{example}</p>
        </>
      )}
    </div>
  );
}

function Table({ rows }: { rows: [string, string, string][] }) {
  return (
    <div className="overflow-x-auto my-6">
      <table className="w-full text-sm border-collapse">
        <thead>
          <tr className="bg-slate-100">
            <th className="border border-slate-200 px-4 py-2 text-left font-semibold text-slate-700">エリア・物件タイプ</th>
            <th className="border border-slate-200 px-4 py-2 text-left font-semibold text-slate-700">表面利回りの目安</th>
            <th className="border border-slate-200 px-4 py-2 text-left font-semibold text-slate-700">特徴</th>
          </tr>
        </thead>
        <tbody>
          {rows.map(([area, rate, note]) => (
            <tr key={area} className="even:bg-slate-50">
              <td className="border border-slate-200 px-4 py-2 text-slate-700">{area}</td>
              <td className="border border-slate-200 px-4 py-2 font-semibold text-blue-700">{rate}</td>
              <td className="border border-slate-200 px-4 py-2 text-slate-600">{note}</td>
            </tr>
          ))}
        </tbody>
      </table>
    </div>
  );
}

